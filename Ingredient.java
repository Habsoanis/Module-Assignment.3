// This is responsibe for the ingredients
public class Ingredient {
   //The variable mIngredient will hold the ingredients
   private final String mIngredients;


//The constructor initializes mIngredients with name which the construtor accepts as
// an arguement
   public Ingredient( String name) {
       this.mIngredients = name  ;
   }


//This method will return the name of an ingredient when called.
   public String getName () {
       return mIngredients;




   }
}



